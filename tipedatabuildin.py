# tuple
# list yang ga bisa diubah
buah = ("apel", "jeruk", "pisang", "tomat")
print(buah)

# dictionary
# json
# key-value
# pemetaan kunci dengan nilai
tempat_tinggal = {
    "budi" : "lampung tengah",
    "Ayip" : "Sukarame",
    "Leenoogs" : "London"
}

print(tempat_tinggal["budi"])

# range
# pencacahan dari a sampai b-1
# parameter ketiga itu menunjukkan langkahnya
jarak = range(1,10)
for x in jarak:
    print(x)

# set
# list yang ga boleh ada duplikat
motor = {"suzuki", "bmw", "toyota", "bmw2"}
print(motor)